public class Pokemon {
    private String name;
    private String type;
    private int power;

    public  String getName() {
        return name;
    }
    public void setName(String n) {
        name = n;
    }
    public  String getType() {
        return type;
    }
    public  void setType(String t) {
        type = t;
    }
    public  int getPower() {
        return power;
    }
    public  void setPower(int p) {
        power = p;
    }

}